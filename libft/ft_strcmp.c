#include <string.h>
#include <stdio.h>
#include "../push_swap.h"

int	ft_strcmp(char *s1, char *s2)
{
	while ((*s1 == *s2) && (*s1 != '\0'))
	{
		s1++;
		s2++;
		if (s1 != s2)
			return (1);
	}
	return (0);
}
